  /// Get nutritional values for a specific ingredient
  /// Returns null if ingredient is not found in database
  static Map<String, double>? getIngredientNutrition(String ingredient) {
    final database = getDatabase();
    final normalizedIngredient = ingredient.toLowerCase().trim();
    return database[normalizedIngredient];
  }

  /// Check if an ingredient exists in the database
  static bool hasIngredient(String ingredient) {
    final database = getDatabase();
    final normalizedIngredient = ingredient.toLowerCase().trim();
    return database.containsKey(normalizedIngredient);
  }

  /// Get all ingredient names in the database
  static List<String> getAllIngredients() {
    return getDatabase().keys.toList()..sort();
  }

  /// Get database statistics
  static Map<String, int> getStats() {
    final database = getDatabase();
    return {
      'totalIngredients': database.length,
      'proteinsCount': database.values.where((nutrition) => nutrition['protein']! > 15).length,
      'vegetablesCount': database.keys.where((ingredient) => 
